-- Queries
SELECT * FROM EMPLOYEES;
SELECT * FROM DEPARTMENTS;
SELECT * FROM EMPLOYEE_DETAILS;

SELECT EMPLOYEES.NAME AS "Employee Name", EMPLOYEES.DEPARTMENT_ID AS "Department ID", DEPARTMENTS.DEPARTMENT_NAME AS "Department Name", DEPARTMENTS.MANAGER_NAME AS "Department Manager"
FROM EMPLOYEES
JOIN DEPARTMENTS ON EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID;

SELECT EMPLOYEES.NAME AS "Employee Name", EMPLOYEES.DEPARTMENT_ID AS "Department ID", DEPARTMENTS.DEPARTMENT_NAME AS "Department Name"
FROM EMPLOYEES
JOIN DEPARTMENTS ON EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID
WHERE DEPARTMENTS.DEPARTMENT_NAME = 'Sales';

SELECT EMPLOYEES.NAME AS "Employee Name", EMPLOYEE_DETAILS.ADDRESS AS "Address"
FROM EMPLOYEES
JOIN EMPLOYEE_DETAILS ON EMPLOYEES.EMPLOYEE_ID = EMPLOYEE_DETAILS.EMPLOYEE_ID;

SELECT EMPLOYEES.EMPLOYEE_ID AS "Employee ID", EMPLOYEES.NAME AS "Employee Name", EMPLOYEES.SALARY AS "Salary"
FROM EMPLOYEES
WHERE EMPLOYEES.SALARY > 60000;

SELECT EMPLOYEES.NAME AS "Employee Name", EMPLOYEES.EMPLOYEE_ID AS "Employee ID", DEPARTMENTS.DEPARTMENT_NAME AS "Department Name", DEPARTMENTS.MANAGER_NAME AS "Department Manager", EMPLOYEES.SALARY AS "Salary", EMPLOYEE_DETAILS.PHONE AS "Phone Number"
FROM EMPLOYEES
JOIN DEPARTMENTS ON EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID
JOIN EMPLOYEE_DETAILS ON EMPLOYEES.EMPLOYEE_ID = EMPLOYEE_DETAILS.EMPLOYEE_ID
WHERE EMPLOYEES.NAME LIKE '%Smith';