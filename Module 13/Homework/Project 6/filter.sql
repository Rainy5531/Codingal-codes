    CUSTOMER_ID INT PRIMARY KEY,
    NAME VARCHAR(50),
    PHONE_NO VARCHAR(10),
    CITY VARCHAR(50)
    PRODUCT_ID INT,
    COST DECIMAL(10, 2)
);

