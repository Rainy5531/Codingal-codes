import turtle

t = turtle.Turtle()
s = turtle.Screen()

s.setup(700, 700)
s.title("Star")
s.bgcolor("Black")
t.hideturtle()

t.penup()
t.goto(0, 0)
t.pendown()
t.width(3)
t.color("white")

t.fillcolor("blue")
t.begin_fill()
t.forward(110)
t.right(108)
t.forward(70)
t.right(108)
t.forward(110)
t.end_fill()

t.fillcolor("yellow")
t.begin_fill()
t.right(180)
t.forward(180)
t.right(108)
t.forward(110)
t.right(144)
t.forward(110)
t.end_fill()

t.fillcolor("red")
t.begin_fill()
t.right(108)
t.forward(180)
t.left(144)
t.forward(110)
t.left(108)
t.forward(70)
t.end_fill()

t.fillcolor("green")
t.begin_fill()
t.right(180)
t.forward(180)
t.left(144)
t.forward(110)
t.left(108)
t.forward(70)
t.end_fill()

t.fillcolor("orange")
t.begin_fill()
t.right(180)
t.forward(180)
t.left(144)
t.forward(110)
t.left(108)
t.forward(70)
t.end_fill()

t.penup()
t.goto(110, 0)
t.pendown()
t.fillcolor("purple")
t.begin_fill()
for _ in range(5):
    t.forward(70)
    t.right(72)
t.end_fill()

t.penup()
while True:
    t.right(1)