num1 = 10
num2 = 4

print("num1 & num2 are:", num1, num2)
print("num1 | num2 =", num1 | num2) #OR operator
print("num1 & num2 =", num1 & num2) #AND operator
print("num1 ^ num2 =", num1 ^ num2) #XOR operator
print("num1 << 2 =", num1 << 2) #Left Shift operator
print("num2 >> 2 =", num2 >> 2) #Right Shift operator
print("~num1 =", ~num1) #negation operator
print("~num2 =", ~num2)

